#ifndef LLVM_DIRECTX_INSTPRINTER_H
#define LLVM_DIRECTX_INSTPRINTER_H

#include "TargetInfo/DirectXFlags.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {
class DirectXInstPrinter : public MCInstPrinter {
public:
  using MCInstPrinter::MCInstPrinter;
  DirectXInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                     const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &O) override;

  // used by DirectXGenAsmWriter.inc
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t>
  getMnemonic(const MCInst &MI) const override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(MCRegister Reg);

private:
};
} // namespace llvm
#endif // LLVM_DIRECTX_INSTPRINTER_H
